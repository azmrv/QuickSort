//! Data Transfer Object (DTO) returned by `ExecuteOperation` and `UndoOperation`.
//!
//! This struct is the Application Layer's response type.  It carries the
//! outcome of an operation across the boundary between the Application
//! and Adapter layers (Tauri commands, IPC handlers).  Adapters map it
//! into whatever format is needed by the UI or external protocol.
//!
//! # Design Decisions
//! - Uses domain types (`OperationId`, `OperationState`) directly to avoid
//!   anemic DTOs and unnecessary mapping.  The Adapter layer is allowed
//!   to depend on domain value objects.
//! - The field `bytes_moved` is retained for backward compatibility with
//!   existing adapters, even though the operation may be a Copy (where no
//!   bytes are "moved").  A future version may rename it to `bytes_processed`
//!   or split it into separate counters.
//! - `processed_files` is a simple count of files that were successfully
//!   handled.  Detailed per-file results (e.g., partial failures) will be
//!   added in a future version.

use serde::{Deserialize, Serialize};
use quicksort_domain::{OperationId, OperationState};

/// Result of a completed or failed operation.
///
/// This DTO is returned by every Use Case that modifies files
/// (`ExecuteOperation`, `UndoOperation`).  It contains enough
/// information for the caller to display a summary, log the event,
/// or trigger further actions.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct OperationResult {
    /// Unique identifier of the operation (generated by the Use Case).
    pub operation_id: OperationId,

    /// Final state of the operation after execution.
    /// - `Completed` – success.
    /// - `Failed` – the operation was attempted but could not be finished.
    /// - `Undone` – a previously completed operation was rolled back.
    pub state: OperationState,

    /// Number of files that were successfully processed.
    pub processed_files: u32,

    /// Total bytes that were moved (or copied, or deleted).
    ///
    /// # Note
    /// Despite the name, this value is also set for Copy and Delete
    /// operations.  It represents the size of the affected files,
    /// not necessarily "moved" bytes.  A more generic name (e.g.,
    /// `bytes_processed`) may be introduced in a future breaking change.
    pub bytes_moved: u64,

    // Future extensions:
    // pub failed_files: Vec<WindowsPath>,
    // pub errors: Vec<String>,
}